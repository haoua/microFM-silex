    'driver'   => 'pdo_mysql',
    'charset'  => 'utf8',
    'host'     => '127.0.0.1',  // Mandatory for PHPUnit testing
    'port'     => '3306',
    'dbname'   => 'silex_api',
    'user'     => 'root',
    'password' => 'root',
);

// enable the debug mode
$app['debug'] = true;